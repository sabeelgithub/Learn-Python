#1)range(stop):
# Generates a sequence of numbers starting from 0 (inclusive) up to, but not including, the specified stop value.
for i in range(5):
    print(i)

for i in range(1, 10, 2):
    print(i)

# 2) range(start, stop)
for i in range(2, 7):
    print(i)

#3) range(start, stop, step)
for i in range(1, 10, 2):
    print(i)


