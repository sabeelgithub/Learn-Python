############## TYPE CASTING ##############

#int()
a = int(3.9)
print(a)

#float()
b = float(3)
print(b)

#str()
c = str(44)
print(c)

#list() => only one argument and it should be iterable
d = list('sabeel')
print(d)
e = list((1,2))
print(e)

#tuple() => only one argument and it should be iterable
f = tuple('sabeel')
print(f)
g = tuple((1,2))
print(g)

# set() => only one argument and it should be iterable
h = set((1,2))
print(h)
i = set('sabeel')
print(i)

# dict()
j = dict([('s',2),('c',2)])
print(j)

############### END TYPE CASTING ##############